# tipe data string (karakter)
nama = "nursakina"
print(nama)
print(type(nama))

#tipe data integer (berisikan angka bilangan bulat)
umur = 18
print(umur)
print(type(umur))

# tipe data float (beriskan angka koma atau bilangna desimal)
tinggi_badan = 1.45
print(tinggi_badan)
print(type(tinggi_badan))

# tipe data boolean ( false and True)
mahasiswa = True 
print(mahasiswa)
print(type(mahasiswa))